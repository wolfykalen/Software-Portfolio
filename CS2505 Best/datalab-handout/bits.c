/* 
 * fitsShort - return 1 if x can be represented as a 
 *   16-bit, two's complement integer.
 *   Examples: fitsShort(33000) = 0, fitsShort(-32768) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 8
 *   Rating: 1
 */
int fitsShort(int x) {
    int shifted = (x << 16) >> 16; // Keep only the lower 16 bits
    return !(shifted ^ x);         // Return 1 if x fits, 0 otherwise
}

/* 
 * anyOddBit - return 1 if any odd-numbered bit in word set to 1
 *   where bits are numbered from 0 (least significant) to 31 (most significant)
 *   Examples anyOddBit(0x5) = 0, anyOddBit(0x7) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 12
 *   Rating: 2
 */
int anyOddBit(int x) {
    int mask = 0xAAAAAAAA;    // Mask for all odd bits
    return !!(x & mask);      // Return 1 if any odd bit is set, 0 otherwise
}

/* 
 * isAsciiDigit - return 1 if 0x30 <= x <= 0x39 (ASCII codes for characters '0' to '9')
 *   Example: isAsciiDigit(0x35) = 1.
 *            isAsciiDigit(0x3a) = 0.
 *            isAsciiDigit(0x05) = 0.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 3
 */
int isAsciiDigit(int x) {
    int lower_bound = (x + ~0x30 + 1) >> 31; // Check if x >= 0x30
    int upper_bound = (0x39 + ~x + 1) >> 31; // Check if x <= 0x39
    return !(lower_bound | upper_bound);     // Return 1 if within range, else 0
}

/* 
 * bang - Compute !x without using !
 *   Examples: bang(3) = 0, bang(0) = 1
 *   Legal ops: ~ & ^ | + << >>
 *   Max ops: 12
 *   Rating: 4 
 */
int bang(int x) {
    return ((x | (~x + 1)) >> 31) + 1; // Returns 1 if x is zero, otherwise 0
}
