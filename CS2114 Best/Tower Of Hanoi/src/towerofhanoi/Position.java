/**
 * Represents the positions of the towers in the Tower of Hanoi game.
 * This enum defines constants for the three positions of the towers
 * LEFT, CENTER, and RIGHT.  
 * The DEFAULT position is used for testing purposes
 * 
 * @author kalendaco
 * @version Mar 25, 2025
 */

public enum Position
{
    /**
     * Represents the left tower position.
     */
    LEFT,
    /**
     * Represents the Center tower position.
     */
    CENTER,
    /**
     * Represents the right tower position.
     */
    RIGHT,
    /**
     * Represents the Default tower position.
     */
    DEFAULT;

}
